package edu.concordia.tsd.util;

public class DeadToggle {
	
	private String toggleValue;
	private String toggleFlagName;
	
	public String getToggleValue() {
		return toggleValue;
	}
	public void setToggleValue(String toggleValue) {
		this.toggleValue = toggleValue;
	}
	public String getToggleFlagName() {
		return toggleFlagName;
	}
	public void setToggleFlagName(String toggleFlagName) {
		this.toggleFlagName = toggleFlagName;
	}

}
